#!/usr/bin/env bash

DIR="$(cd "$( dirname "${BASH_SOURCE[0]}" )" >/dev/null && pwd)"
ROOT="$(dirname $DIR)"

OG="$ROOT/node_modules"
TMP="$ROOT/node_modules_tmp"

PATCHED="$ROOT/.node_modules_patched"

function patch {
    touch $PATCHED
    mv $OG $TMP
}

function unpatch {
    rm $PATCHED
    mv $TMP $OG
}

if [ -d $OG ] && [ ! -f $PATCHED ]; then
    patch
    trap unpatch EXIT
fi
